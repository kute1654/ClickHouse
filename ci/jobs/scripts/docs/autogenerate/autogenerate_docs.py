    ("Hash", "hash-functions"), ("Introspection", "introspection"),
    ("IP Address", "ip-address-functions"), ("JSON", "json-functions"),
    ("Logical", "logical-functions"), ("Machine Learning", "machine-learning-functions"),
    ("Mathematical", "math-functions"), ("Natural Language Processing", "nlp-functions"),
    ("Random Number", "random-functions"),
