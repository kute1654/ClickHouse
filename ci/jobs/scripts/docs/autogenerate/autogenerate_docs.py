FUNCTION_GENERATORS = [
    {
        "name": f"functions:{cat}",
        "sql": ["generate-functions.sql"],
        "params": {"category": cat},
        "outfile": "temp-functions.md",
        "dest": f"docs/sql-reference/functions/{dest}.md",
        "method": "markers",
    }
    for cat, dest in FUNCTION_CATEGORIES
]

def aggregate_generators(docs_dir):
    # Aggregate functions are driven by the existing doc pages (one per function)
    # rather than a fixed list: for each page that has the AUTOGENERATED markers,
    # regenerate its body via generate-aggregate-functions.sql. Pages whose name
    # is not an aggregate function produce empty SQL output and are skipped.
    agg_dir = os.path.join(docs_dir, "reference", "functions", "aggregate-functions")
    gens = []
    for name in sorted(os.listdir(agg_dir)) if os.path.isdir(agg_dir) else []:
        if not name.endswith(".mdx") or name == "index.mdx":
            continue
        fn = name[:-len(".mdx")]
        with open(os.path.join(agg_dir, name), encoding="utf-8") as f:
            if not START_RE.search(f.read()):
                continue
        gens.append({
            "name": f"aggregate:{fn}",
            "sql": ["generate-aggregate-functions.sql"],
            "params": {"function_name": fn},
            "outfile": "temp-aggregate-function.md",
            "dest": f"docs/sql-reference/aggregate-functions/reference/{fn}.md",
            "method": "markers",
            "skip_if_empty": True,  # non-aggregate name -> empty output -> skip
        })
    return gens


ALL_GENERATORS = SETTINGS_GENERATORS + FUNCTION_GENERATORS


FRONTMATTER_RE = re.compile(r"\A---\n.*?\n---\n?", re.DOTALL)


def import_migrate(docs_dir):
    # The Docusaurus->Mintlify body transforms (imports @theme->/snippets,
    # HTML->MDX comments, link rewriting) are reused from the migration script
    # so generated pages match migrated ones exactly -- a single source of truth.
    sys.path.insert(0, os.path.join(docs_dir, "_migration"))
    import migrate  # noqa: E402
    return migrate


def build_file_map(migrate, slug_map):
    # docusaurus_file -> mintlify_file, plus migrate's link/import Lookups.
    lk, rows = migrate.build_lookups(Path(slug_map))
    file_map = {}
    for r in rows:
        mfile = (r.get("mintlify_file") or "").split(" | ")[0].strip()
        if r.get("status") == "matched" and mfile:
            file_map[(r.get("docusaurus_file") or "").strip()] = mfile
    return lk, file_map


def transform_body(migrate, content, src_docu, dest_path, lk):
    # Apply the same body transforms migrate.py runs on a migrated page.
    issues = []
    content, _ = migrate.transform_imports(
        content, lk, issues, source_docu_path=Path(src_docu), dest_path=Path(dest_path)
    )
    content = migrate.transform_html_comments(content)
    content = migrate.rewrite_links(content, src_docu, lk, issues, dest_path=Path(dest_path))
    return content


def run_sql(binary, sql_path, cwd, params=None):
    # Run the generator SQL; it writes its INTO OUTFILE markdown into `cwd`.
    cmd = [binary, "local", "--queries-file", sql_path]
    for k, v in (params or {}).items():
        cmd.append(f"--param_{k}={v}")
    subprocess.run(cmd, cwd=cwd, check=True)


def replace_between_markers(page, content):
    start = START_RE.search(page)
    end = END_RE.search(page, start.end()) if start else None
    if not start or not end:
        raise ValueError("destination is missing the AUTOGENERATED markers")
    # Keep the marker lines themselves; replace only the region between them.
    return page[:start.end()] + "\n" + content.strip("\n") + "\n" + page[end.start():]


def generate(gen, binary, docs_dir, repo_root, migrate, lk, file_map, remap):
    scratch = tempfile.mkdtemp(prefix="autogen-")
    # Stage the files the SQL reads via file() (C++ sources, etc.) into scratch.
    for staged, src in gen.get("deps", {}).items():
        shutil.copyfile(os.path.join(repo_root, src), os.path.join(scratch, staged))
    for sql in gen["sql"]:
        run_sql(binary, os.path.join(SQL_DIR, sql), scratch, gen.get("params"))
    with open(os.path.join(scratch, gen["outfile"]), encoding="utf-8") as f:
        content = f.read()
    if gen.get("skip_if_empty") and not content.strip():
        return None, None  # e.g. a page whose name is not an aggregate function

    dest_rel = file_map.get(gen["dest"]) if remap else re.sub(r"^docs/", "", gen["dest"])
    if not dest_rel:
        raise ValueError(f"no Mintlify destination for {gen['dest']}")
    dest = os.path.join(docs_dir, dest_rel)

    if gen.get("fixups"):
        content = apply_fixups(content)
    if remap:
        content = transform_body(migrate, content, gen["dest"], dest, lk)

    if gen["method"] == "markers":
        with open(dest, encoding="utf-8") as f:
            new = replace_between_markers(f.read(), content)
    elif gen["method"] == "body":
        # Keep the Mintlify page's frontmatter; replace the rest with the
        # generated body (its own Docusaurus frontmatter stripped).
        with open(dest, encoding="utf-8") as f:
            fm = FRONTMATTER_RE.match(f.read())
        body = FRONTMATTER_RE.sub("", content, count=1).lstrip("\n")
        new = (fm.group(0).rstrip("\n") + "\n\n" + body) if fm else body
    else:  # overwrite
        new = content
    return dest, new


def main(argv=None):
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--binary", default="build/programs/clickhouse")
    p.add_argument("--docs-dir", default="docs")
    p.add_argument("--slug-map",
                   help="default: <docs-dir>/_migration/slug-map.csv")
    mode = p.add_mutually_exclusive_group()
    mode.add_argument("--write", action="store_true", help="update docs in place")
    mode.add_argument("--check", action="store_true",
                      help="fail if regenerated output differs from committed docs")
    p.add_argument("--no-remap-legacy", dest="remap", action="store_false",
                   help="emit paths/links as the SQL produces them (Mintlify-native)")
    p.add_argument("--only", help="only run generators whose name contains this substring")
    args = p.parse_args(argv)

    binary = os.path.abspath(args.binary)
    docs_dir = os.path.abspath(args.docs_dir)
    repo_root = os.path.dirname(docs_dir)
    slug_map = args.slug_map or os.path.join(docs_dir, "_migration", "slug-map.csv")

    migrate = lk = None
    file_map = {}
    if args.remap:
        migrate = import_migrate(docs_dir)
        lk, file_map = build_file_map(migrate, slug_map)

    all_generators = ALL_GENERATORS + aggregate_generators(docs_dir)
    generators = [g for g in all_generators if not args.only or args.only in g["name"]]

    drift = 0
    for gen in generators:
        dest, new = generate(gen, binary, docs_dir, repo_root, migrate, lk, file_map, args.remap)
        if new is None:  # skipped (e.g. non-aggregate page)
            continue
        rel = os.path.relpath(dest, docs_dir)
        if args.check:
            current = open(dest, encoding="utf-8").read() if os.path.isfile(dest) else ""
            if current != new:
                print(f"DRIFT: {rel} is stale")
                drift += 1
            else:
                print(f"ok: {rel}")
        elif args.write:
            with open(dest, "w", encoding="utf-8") as f:
                f.write(new)
            print(f"wrote: {rel}")
        else:
            print(f"[dry-run] would update: {rel}")
    return 1 if drift else 0


if __name__ == "__main__":
    sys.exit(main())
